	<h4>What is sustainability?</h4>

	<p>
		The word "sustainability" is a word that's thrown around a lot today to describe many different things in many different contexts. It is apparent that many people don't really know what it means. We think that, as human beings living on earth, it is important for each one of us to have a basic understanding of what sustainability is. 
	</p>

	<p>According to <a href = "http://www.thwink.org/">thwink.org</a>, sustainability is "the ability to continue a defined behavior indefinitely". A more practical definition, in the context of human society, includes three main facets: environmental, economic, and social. </p>
	<ul style = "margin-left: 4rem; list-style: none;" >
		<li>Environmental sustainability means using resources at an average rate that is less than their average rate of replenishment, as well as creating pollution at a rate that is less than the rate it takes for the environment to process that pollution.</li>
		<li>Economic sustainability means that the economy supports a certain level of economic production indefinitely.</li>
		<li>Social sustainability means that the society functions at a certain level of social well-being indefinitely.</li>
	</ul>
